const FaqPage = () => {
	const [openIndex, setOpenIndex] = useState<string | null>(null);

	const toggleItem = (key: string) => {
		setOpenIndex((prev) => (prev === key ? null : key));
	};

	return (
		<div className="p-faq l-page">
			<div className="p-faq__title">よくある質問</div>
			<Border />

			<div className="p-faq__content">
				{faqData.map((category, catIdx) => (
					<div key={category.title} className="p-faq__category">
						<div className="p-faq__category-title">
							{category.title}
						</div>
						<div className="p-faq__items">
							{category.items.map((item, itemIdx) => {
								const key = `${catIdx}-${itemIdx}`;
								const isOpen = openIndex === key;
								return (
									<div
										key={key}
										className={`p-faq__item ${isOpen ? "-open" : ""}`}
									>
										<div
											className="p-faq__question"
											onClick={() => toggleItem(key)}
										>
											<span className="p-faq__question-label">Q.</span>
											<span className="p-faq__question-text">
												{item.question}
											</span>
											<span className={`p-faq__arrow ${isOpen ? "-open" : ""}`}>
												&#9662;
											</span>
										</div>
										{isOpen && (
											<div className="p-faq__answer">
												<span className="p-faq__answer-label">A.</span>
												<span className="p-faq__answer-text">
													{item.answer}
												</span>
											</div>
										)}
									</div>
								);
							})}
						</div>
					</div>
				))}
			</div>
		</div>
	);
};

export default FaqPage;
